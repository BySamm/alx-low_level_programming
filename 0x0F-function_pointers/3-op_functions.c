#include <stdio.h>
#include <stdlib.h>

/**
 * op_add - Function to add two numbers
 * @a: First number
 * @b: Second number
 * Return: Nothing
 */

int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - Function to substract two numbers
 * @a: First number
 * @b: Second number
 * Return: Nothing
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Function to multiply two number
 * @a: First number
 * @b: Second number
 * Return: Nothing
 */


int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Function to divide two number
 * @a: First number
 * @b: Second number
 * Return: Nothing
 */


int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Function to find the remainder of division
 * @a: First number
 * @b: Second number
 * Return: Nothing
 */


int op_mod(int a, int b)
{
	return (a % b);
}
